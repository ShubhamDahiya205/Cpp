#include <stdio.h>

void main() {
    int a, b;
    printf("Enter value of a = ");
    scanf("%d", &a);
    printf("Enter value of b = ");
    scanf("%d", &b);

    printf("Value of a + b is %d \n", a+b);
    printf("Value of a - b is %d \n", a-b);
    printf("Value of a * b is %d \n", a*b);
    printf("Value of a / b is %d \n", a/b);
}